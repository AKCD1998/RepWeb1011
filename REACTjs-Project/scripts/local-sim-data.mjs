function makeBarcode(seed) {
  return `8851011${String(seed).padStart(6, "0")}`;
}

function ingredient(code, nameEn, nameTh, strengthNumerator, numeratorUnitCode, extra = {}) {
  return {
    code,
    nameEn,
    nameTh,
    strengthNumerator,
    numeratorUnitCode,
    strengthDenominator: extra.strengthDenominator ?? null,
    denominatorUnitCode: extra.denominatorUnitCode ?? null,
  };
}

